#pragma once

// =============================================================================
// SceneBase.h
// すべてのシーン（画面）が共通で持つ「型（インターフェース）」を定義します。
//
// 【この設計の考え方】
// タイトル・メイン・リザルトは見た目も処理も違いますが、
// 「初期化する」「更新する」「描画する」という流れは同じです。
// そこで共通の親クラス SceneBase を作り、SceneManager が
// 「今どのシーンでも同じ手順で動かせる」ようにします。
// =============================================================================

// シーンの種類を表す列挙型（enum）
// 遷移先を SceneManager に伝えるときに使います
enum class SceneID
{
	None,       // 遷移しない（今のシーンのまま）
	Title,      // タイトル画面
	Main,       // メインゲーム画面（クリッカー本体）
	Result,     // リザルト画面
	Ranking,    // ランキング表示
};

// すべてのシーンの親クラス（抽象クラス）
// 純粋仮想関数 (= 0) を使うことで、子クラスで必ず実装させます
class SceneBase
{
public:
	// 仮想デストラクタ … ポインタ経由で delete するときに必要
	virtual ~SceneBase() {}

	// シーンに入った直後に1回だけ呼ばれる初期化処理
	// 画像の読み込み、変数のリセットなどをここに書きます
	virtual void Init() = 0;

	// 毎フレーム呼ばれる更新処理（入力・ゲームロジック）
	virtual void Update() = 0;

	// 毎フレーム呼ばれる描画処理
	virtual void Draw() = 0;

	// シーン遷移が必要かどうかを返す
	// デフォルトは「遷移しない」。各シーンで必要なら上書きします
	virtual SceneID GetNextSceneID() const { return SceneID::None; }
};
